/**
 * Defines the constants used to indicate the different types of nodes that can
 * occur in the DOM.
 *
*
 */
public final class NodeType {

  private NodeType() {}

  /**
   * Constant representing the Element node type.
   */
  public static short ELEMENT_NODE = 1;

  /**
   * Constant representing the Text node type.
   */
  public static short TEXT_NODE = 3;

}
